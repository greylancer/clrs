class Stack:
    def __init__(self, A):
        self.A = A

    def is_empty():
        if len(self.A) == 0:
            return True
        else:
            return False

    def push(x):
        self.A,append(x)

    def pop():
        if len(self.A) == 0:
            return None
        x = self.A[-1]
        del self.A[-1]
        return x